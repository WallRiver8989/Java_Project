    private void btnLuuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLuuMouseClicked
        // TODO add your handling code here:
        String id=txtAdminID.getText();         
        String n=txtAdminName.getText(); 
        String account=txtAdminAccount.getText(); 
        String pw=txtAdminPW.getText(); 
        String mail=txtAdminMail.getText(); 
        String tel=txtAdminTel.getText(); 
        if(n.length() == 0 || account.length() == 0 || pw.length() == 0 || mail.length() == 0 || tel.length()== 0){
            JOptionPane.showMessageDialog(null,"Vui lòng kiểm tra lại thông tin!","Thông báo",1);
        }
        
        else{
            try{
                if(cothem == true){
                    admin.InsertData(n, account, pw, mail, tel);
                }
                else{
                    admin.EditData(id, n, account, pw, mail, tel);
                }
                ClearData();
                ShowData();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null,"Thao tác không thành công","Thông báo",1);
            }
            setKhoa(true);              
            setButton(true);
            setNull();
        }
    }//GEN-LAST:event_btnLuuMouseClicked

