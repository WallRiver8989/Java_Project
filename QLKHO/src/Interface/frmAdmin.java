    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        // TODO add your handling code here:
        String id=txtAdminID.getText();         
        try {             
            if(id.length()==0){                              
                JOptionPane.showMessageDialog(null,"Vui long cho du lieu de xoa", "Thong bao",1);
            }             
            else {                 
                if(JOptionPane.showConfirmDialog(null, "Ban co muon xoa admin voi id : " + id + "  nay hay khong?","Thong bao",2)==0){                         
                    admin.DeleteData(id);//goi ham xoa du lieu theo ma loai 
                    ClearData();//Xoa du lieu trong tableModel                     
                    ShowData();//Do du lieu vao table Model                     
                    setNull();//Xoa trang Textfield  
                }
            }                                           
        }
        catch (SQLException ex){             
            JOptionPane.showMessageDialog(null,"Xoa that bai","Thong bao",1);  
        }
    }//GEN-LAST:event_btnXoaMouseClicked

