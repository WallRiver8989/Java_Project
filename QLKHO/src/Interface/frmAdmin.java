    private void jTableAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAdminMouseClicked
        // TODO add your handling code here:
        try{             
        //Lay chi so dong dang chon                 
            int row =this.jTableAdmin.getSelectedRow();             
            String id=(this.jTableAdmin.getModel().getValueAt(row,0)).toString();             
            ResultSet rs= admin.ShowAdmin(id);//Goi ham lay du lieu theo ma loai             
            if(rs.next()){//Neu co du lieu             
                this.txtAdminID.setText(rs.getString("ID"));              
                this.txtAdminName.setText(rs.getString("Name"));
                this.txtAdminAccount.setText(rs.getString("Account"));  
                this.txtAdminPW.setText(rs.getString("PasswordAdmin"));  
                this.txtAdminMail.setText(rs.getString("Email"));  
                this.txtAdminTel.setText(rs.getString("Tel"));  
            }         
        }      
        catch (SQLException e) {         
        }
    }//GEN-LAST:event_jTableAdminMouseClicked

