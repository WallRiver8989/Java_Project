    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        // TODO add your handling code here:
        String id = (txtID.getText());
        String name=txtName.getText();         
        String idCate=(txtCategory.getText());  
        String idBranch=(txtBranch.getText());        
        String quantity= (txtQuantity.getText());  
        String description=txtfDescription.getText();  
        
        if(name.length() == 0 || idCate.length() == 0 ||idBranch.length() == 0 || quantity.length() == 0 ){
            JOptionPane.showMessageDialog(null,"Vui lòng kiểm tra lại thông tin!","Thông báo",1);
        }
        
        else{      
             try {                 
                 if(cothem==true)    //Luu cho tthem moi                                
                     product.InsertData(name,idCate,idBranch,quantity,description);                
                 else                //Luu cho sua                     
                     product.EditData(id,name,idCate,idBranch,quantity,description);                 
                 ClearData(); //goi ham xoa du lieu tron tableModel                 
                 ShowData(); //Do lai du lieu vao Table Model               
             } catch (SQLException ex) {                    
                 JOptionPane.showMessageDialog(null,"Cap nhat that bai", "Thong bao",1);               
                                  setLock(false);              
                                  setButton(true);          
             }    
    }//GEN-LAST:event_btnSaveMouseClicked
    }
