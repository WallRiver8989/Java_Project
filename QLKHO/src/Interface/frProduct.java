      public void ShowData() throws SQLException{
          ResultSet result = product.ShowProduct();
          
                     try {
                         while(result.next()){ // nếu còn đọc tiếp được một dòng dữ liệu
                             String rows[] = new String[6];                 
                             rows[0] = result.getString(1); // lấy dữ liệu tại cột số 1 
                             rows[1] = result.getString(2); // lấy dữ liệu tai cột số 2 
                              rows[2] = result.getString(3); // lấy dữ liệu tại cột số 3
                             rows[3] = result.getString(4); // lấy dữ liệu tai cột số 4
                              rows[4] = result.getString(5); // lấy dữ liệu tại cột số 5 
                             rows[5] = result.getString(6); // lấy dữ liệu tai cột số 6
                              
                             tableModel.addRow(rows); // đưa dòng dữ liệu vào tableModel  
                             
                             //mỗi lần có sự thay đổi dữ liệu ở tableModel thì Jtable sẽ tự động update  
    
                         }
                     }
                      catch (SQLException e) {         }
      }
    //Ham xoa du lieu trong tableModel     
      public void ClearData() throws SQLException{          //Lay chi so dong cuoi cung          
      int n=tableModel.getRowCount()-1;          
      for(int i=n;i>=0;i--)             
          tableModel.removeRow(i);//Remove tung dong    
      }
      
