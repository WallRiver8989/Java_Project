    //Click into Table
    private void tableBranchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBranchMouseClicked
        // TODO add your handling code here:
        try{
            int row = this.tableBranch.getSelectedRow();
            String id = (this.tableBranch.getModel().getValueAt(row, 0)).toString();
            
            //Get Data Branch by id
            ResultSet rs = branch.ShowBranch(id);
            
            if(rs.next()){
                this.txtIDBranch.setText(rs.getString("ID"));
                this.txtNameBranch.setText(rs.getString("Name"));
                this.txtTel.setText(rs.getString("Tel"));
                this.txtAddressBranch.setText(rs.getString("AddressBranch"));
                
            }
        }
        catch(SQLException e){
            
        }
    }//GEN-LAST:event_tableBranchMouseClicked

