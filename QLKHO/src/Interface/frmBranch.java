    //Del Button
    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        String id = this.txtIDBranch.getText();
        String name = this.txtNameBranch.getText();
        try{
            if(id.length() == 0){
                JOptionPane.showMessageDialog(null,"Vui lòng chọn dữ liệu cần xóa!","Thông báo",1);
            }
            else{
                if(JOptionPane.showConfirmDialog(null, "Bạn có thực sự muốn xóa Nhà Cung Cấp " + name + " không?","Thông báo",2)==0){
                    branch.DelData(id);
                    ClearData();
                    ShowData();
                    setNull();
                }
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Thao tác không thành công!","Thông báo",1);
        }
    }//GEN-LAST:event_btnDelActionPerformed
    
